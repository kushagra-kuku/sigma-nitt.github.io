function showFourthYears() {
	for(let member = 0; member < fourth_years.length; member++) {
		if(fourth_years[member].linkedinURL !== "")
			withLinkedinURL(member, fourth_years);
		else
			withoutLinkedinURL(member, fourth_years);
	}
}

function showThirdYears() {
	for(let member = 0; member < third_years.length; member++) {
		if(third_years[member].linkedinURL !== "")
			withLinkedinURL(member, third_years);
		else
			withoutLinkedinURL(member, third_years);
	}
}



function showAlumni() {
	for(let member = 0; member < alumni_members.length; member++) {
		if(alumni_members[member].linkedinURL !== "")
			withLinkedinURL(member, alumni_members);
		else 
			withoutLinkedinURL(member, alumni_members);
	}
}

