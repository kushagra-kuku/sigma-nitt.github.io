function withLinkedinURL(member, year) {
	members.innerHTML += `<div class="col-new-lg-3 col-new-md-4 col-new-sm-6 col-new-xs-12 card" data-aos="flip-right" data-aos-duration="2000">
								<div class="card-body">
								<a href="${year[member].linkedinURL}" target="_blank">
									<img alt="photo" src="../img/members/${year[member].image}" class="members-image" width="100%">
									<div class="member-title mb-4" style="text-align: center; font-size: 2.5vh; color: darkblue;">
										${year[member].name}
									</div>
									<div class="member-title mb-4" style="text-align: center; font-size: 2vh; color: maroon; height: 40px;">
										${year[member].position}
									</div>
								</a>
								</div>
							</div>`
}

